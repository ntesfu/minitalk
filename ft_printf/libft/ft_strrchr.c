char	*ft_strrchr(const char *s, int c)
{
	char	*loc;

	loc = 0;
	while (*s)
	{
		if (*s == (c % 256))
			loc = (char *)s;
		s++;
	}
	if (c == '\0')
		loc = (char *)s;
	return (loc);
}
